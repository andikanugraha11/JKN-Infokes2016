        <div class="container inner">
            <div class="row story">
                <div class="col-sm-6">
                    <div class="col-wrapper">
                        <img class="bottom-marged" src="img/LogoJKN.png">
                    </div>
                </div>
                <div class="col-sm-5">
                    <div class="col-wrapper">
                        <p align="justify"><span class="up-caps">K</span>esehatan adalah hak dasar setiap orang, dan semua warga negara berhak mendapatkan pelayanan kesehatan. UUD 1945 mengamanatkan bahwa jaminan kesehatan bagi masyarakat, khususnya yang miskin dan tidak mampu, adalah tanggung jawab pemerintah pusat dan daerah. Pada UUD 1945 Perubahan, Pasal 34 ayat 2 menyebutkan bahwa negara mengembangkan Sistem Jaminan Sosial bagi seluruh rakyat Indonesia. Pemerintah menjalankan UUD 1945 tersebut dengan mengeluarkan UU No 40 Tahun 2004 tentang Sistem Jaminan Sosial Nasional (SJSN) untuk memberikan jaminan sosial menyeluruh bagi setiap orang dalam rangka memenuhi kebutuhan dasar hidup yang layak menuju terwujudnya masyarakat Indonesia yang sejahtera, adil, dan makmur. Dalam UU No 36 Tahun 2009 tentang Kesehatan juga ditegaskan bahwa setiap orang mempunyai hak yang sama dalam memperoleh akses atas sumber daya di bidang kesehatan dan memperoleh pelayanan kesehatan yang aman, bermutu, dan terjangkau.</p>

                        <p align="justify">
                            Sesuai dengan UU No 40 Tahun 2004, SJSN diselenggarakan dengan mekanisme Asuransi Sosial dimana setiap peserta wajib membayar iuran guna memberikan perlindungan atas risiko sosial ekonomi yang menimpa peserta dan/atau anggota keluarganya. Dalam SJSN, terdapat Jaminan Kesehatan Nasional (JKN) yang merupakan bentuk komitmen pemerintah terhadap pelaksanaan jaminan kesehatan masyarakat Indonesia seluruhnya. Sebelum JKN, pemerintah telah berupaya merintis beberapa bentuk jaminan sosial di bidang kesehatan, antara lain Askes Sosial bagi pegawai negeri sipil (PNS), penerima pensiun dan veteran, Jaminan Pemeliharaan Kesehatan (JPK) Jamsostek bagi pegawai BUMN dan swasta, serta Jaminan Kesehatan bagi TNI dan Polri. Untuk masyarakat miskin dan tidak mampu, sejak tahun 2005 Kementerian Kesehatan telah melaksanakan program jaminan kesehatan sosial, yang awalnya dikenal dengan nama program Jaminan Pemeliharaan Kesehatan bagi Masyarakat Miskin (JPKMM), atau lebih populer dengan nama program Askeskin (Asuransi Kesehatan Bagi Masyarakat Miskin). Kemudian sejak tahun 2008 sampai dengan tahun 2013, program ini berubah nama menjadi program Jaminan Kesehatan Masyarakat (Jamkesmas).
                        </p>
						<p align="justify">Seiring dengan dimulainya JKN per 1 Januari 2014, semua program jaminan kesehatan yang telah dilaksanakan pemerintah tersebut (Askes PNS, JPK Jamsostek, TNI, Polri, dan Jamkesmas), diintegrasikan ke dalam satu Badan Penyelenggara Jaminan Sosial Kesehatan (BPJS Kesehatan). Sama halnya dengan program Jamkesmas, pemerintah bertanggungjawab untuk membayarkan iuran JKN bagi fakir miskin dan orang yang tidak mampu yang terdaftar sebagai peserta Penerima Bantuan Iuran (PBI).</p>

