			<h2 align="center">Prosedur Pendaftaran</h2>
				  <p align="justify">Pendaftaran kepersetaan JKN adalah kewajiban Peserta dan Pemberi Kerja. Khusus bagi orang miskin dan tidak mampu, Pemerintah berkewajiban mendaftarkan mereka dan meyubsidi iuran, serta membayarkan iuran JKN kepada BPJS Kesehatan</p> 
<h3>Prosedur Pendaftaran :</h3> 
<h5>Prosedur pendaftaran peserta JKN pada awal bulan Januari 2014 terdiri atas pendaftaran otomatis, pendaftaran ulang, dan pendaftaran peserta baru.
				  <h5>1.	PENDAFTARAN OTOMATIS (DENGAN SENDIRINYA MENJADI PESERTA)</h5>
	<h5>&nbsp;&nbsp;&nbsp;&nbsp;Kepersetaan program jaminan kesehatan/pelayanan kesehatan berpindah dengan sendirinya tanpa perlu mendaftar ulang ke BPJS Kesehatan, bagi:</h5>
	<h5>&nbsp;&nbsp;&nbsp;&nbsp;(1)	Peserta Akses Sosial (Pegawai Negeri Sipil(PNS)), Pensiunan PNS, Pensiunan TNI/POLRI Kemerdekaan, Anggota Legium Veteran, beserta keluarganya.
		<h5>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kartu Akses Sosial tetap berlaku untuk pelayanan kesehatan sepanjang BPJS Kesehatan belum menerbitkan kartu JKN.</h5>
		<h5>&nbsp;&nbsp;&nbsp;&nbsp;(2)	Anggota POLRI dan Prajurit TNI beserta keluarganya.</h5>
		<h5>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kartu Tanda Anggota POLRI atau Nomor Registrasi Pokok TNI berlaku sebagai kartu peserta JKN sepanjang BPJS Kesehatan belum menerbitkan karti JKN.</h5>
		<h5>&nbsp;&nbsp;&nbsp;&nbsp;(3)	Peserta Program Jaminan Kesehatan Masyarakat (JAMKESMAS).</h5>
		<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kartu JAMKESMAS tetap beerlaku unutk pelayanan kesehatan sepanjang BPJS Kesehatan belum menerbitkan kartu JKN.</p>

<h5>2.	PENDAFTARANN ULANG DAN PERUBAHAN KEPERSEETAAN</h5>
	<p align="justify">&nbsp;&nbsp;&nbsp;&nbsp;Kepersetaan Program Jaminan Pemeliharaan Kesehatan (JPK) Jamsostek berpindah dengan sendirinya menjadi kepersetaan JKN disertai persyaratan wajib daftar ulang kepada BPJS Kesehatan. Kartu JPK Jamsostek tetap berlaku untuk pelayanan kesehatan sepanjang peserta membayar iuran dan BPJS Kesehatan belum menerbitkan kartu JKN. Perusahaan mendaftarkan ulang pekerja dan perusahaannya kepada BPJS Kesehatan dengan mengisi formulir pendaftaran dan formulir data pekerja, serta membayar iuran. </p>

		<h5>3.	PENDAFTARAN PESERTA BARU</h5>
<h5>&nbsp;&nbsp;&nbsp;&nbsp;Perusahaan atau penduduk yang belum mengikut program jaminan kesehatan, diwajibkan untuk segera mendaftar ke Kantor BPJS Kesehatan terdekat dengan melengkapi administrasi pedaftaran.</h5>
		<h5>Dokumen pendukung untuk pendaftaran perusahaan dan pekerjanya:</h5>
		<h5>(1)	Mengisi formulir pendaftaran</h5>
		<h5>(2)	Mengisi data calon peserta</h5>
		<h5>(3)	NPWP Perushaan</h5>
		<h5>(4)	Surat Izin Usaha Perdagangan Perusahaan</h5>
		<h5>(5)	Foto kopi KTP Pekerja</h5>
		<h5>(6)	Foto kopi Kartu Keluarga Pekerja</h5>
		<h5>(7)	Pas foto 3x4 berwarna 2 lembar (masing-masng peserta)</h5>
		<h5>Dokumen pendukung untuk pendaftaran perorangan, adalah :</h5>
		<h5>(1)	Mengisi formulir Pendaftaran</h5>
		<h5>(2)	Foto kopi KTP</h5>
		<h5>(3)	Foto kopi Kartu Keluarga</h5>
		<h5>(4)	Pas foto 3x4 berwarna 2 lembar (masing-masing peserta)</h5>
</p>
